'use strict'
const winston = require('winston')
const path = require('path')
const fs = require('fs')
const config = require('@app/config')

class Logger {
    static logger = null

    static init() {
        const logDir = path.join(__dirname, '..', config.app.log_dir)

        if (!fs.existsSync(logDir)) {
            fs.mkdirSync(logDir, { recursive: true })
        }

        const customFormat = winston.format.printf((info) => {
            const timestamp = info.timestamp
            const layer = info.context ? info.context.toUpperCase() : 'SYSTEM'
            const status = info.level.toUpperCase()
            const message = info.stack || info.message
            return `${timestamp} | ${layer} | ${status} : ${message}`
        })

        const logFormat = winston.format.combine(
            winston.format.timestamp({ format: 'YYYY-MM-DD HH:mm:ss' }),
            winston.format.errors({ stack: true }),
            customFormat
        )

        const consoleFormat = winston.format.combine(
            winston.format.timestamp({ format: 'YYYY-MM-DD HH:mm:ss' }),
            winston.format.errors({ stack: true }),
            winston.format.printf((info) => {
                const timestamp = info.timestamp
                const layer = info.context ? info.context.toUpperCase() : 'SYSTEM'

                const colors = {
                    error: '\x1b[31m',
                    warn: '\x1b[33m',
                    info: '\x1b[32m',
                    debug: '\x1b[36m',
                    reset: '\x1b[0m',
                }

                const levelColor = colors[info.level] || colors.reset
                const status = info.level.toUpperCase()
                const message = info.stack || info.message

                return `${timestamp} | ${layer} | ${levelColor}${status}${colors.reset} : ${message}`
            })
        )

        this.logger = winston.createLogger({
            level: config.app.production ? 'info' : 'debug',
            format: logFormat,
            transports: [
                new winston.transports.File({
                    filename: path.join(logDir, 'error.log'),
                    level: 'error',
                    maxsize: 5242880,
                    maxFiles: 5,
                }),
                new winston.transports.File({
                    filename: path.join(logDir, 'combined.log'),
                    maxsize: 5242880,
                    maxFiles: 5,
                }),
                new winston.transports.Console({
                    format: consoleFormat,
                }),
            ],
        })
    }

    static info(context, message) {
        if (!this.logger) this.init()
        this.logger.info({ message, context })
    }

    static error(context, message) {
        if (!this.logger) this.init()
        this.logger.error({ message, context })
    }

    static warn(context, message) {
        if (!this.logger) this.init()
        this.logger.warn({ message, context })
    }

    static debug(context, message) {
        if (!this.logger) this.init()
        this.logger.debug({ message, context })
    }

    static set(error, context) {
        if (!this.logger) this.init()
        const errorMessage = error.stack || error.message || error
        this.logger.error({
            message: errorMessage,
            context,
        })
    }
}

module.exports = Logger
